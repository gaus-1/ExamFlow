retry_db_operation() {
    local command="$1"
    local max_attempts=3
    local delay=10
    
    for attempt in $(seq 1 $max_attempts); do
        echo "üîÑ –ü–æ–ø—ã—Ç–∫–∞ $attempt/$max_attempts: $command"
        
        if eval "$command"; then
